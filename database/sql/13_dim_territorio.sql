CREATE OR REPLACE VIEW vw_territorial_inconsistencias AS
SELECT
    'EVENTO_COMISARIA' AS tipo_inconsistencia,
    e.id_evento::TEXT AS referencia,
    e.distrito AS distrito_registro,
    c.distrito AS distrito_referencia,
    e.id_comisaria::TEXT AS referencia_relacionada,
    e.id_territorio_distrito AS id_territorio_registro,
    c.id_territorio_distrito AS id_territorio_referencia
FROM eventos_delictivos e
INNER JOIN comisarias c
    ON c.id_comisaria = e.id_comisaria
WHERE e.id_comisaria IS NOT NULL
  AND e.id_territorio_distrito IS DISTINCT FROM c.id_territorio_distrito

UNION ALL

SELECT
    'ZONA_COMISARIA' AS tipo_inconsistencia,
    z.id_zona::TEXT AS referencia,
    z.distrito AS distrito_registro,
    c.distrito AS distrito_referencia,
    z.id_comisaria::TEXT AS referencia_relacionada,
    z.id_territorio_distrito AS id_territorio_registro,
    c.id_territorio_distrito AS id_territorio_referencia
FROM zonas_operativas z
INNER JOIN comisarias c
    ON c.id_comisaria = z.id_comisaria
WHERE z.id_comisaria IS NOT NULL
  AND z.id_territorio_distrito IS DISTINCT FROM c.id_territorio_distrito

UNION ALL

SELECT
    'HOTSPOT_ZONA' AS tipo_inconsistencia,
    h.id_hotspot::TEXT AS referencia,
    h.distrito AS distrito_registro,
    z.distrito AS distrito_referencia,
    h.id_zona::TEXT AS referencia_relacionada,
    h.id_territorio_distrito AS id_territorio_registro,
    z.id_territorio_distrito AS id_territorio_referencia
FROM hotspots h
INNER JOIN zonas_operativas z
    ON z.id_zona = h.id_zona
WHERE h.id_zona IS NOT NULL
  AND h.id_territorio_distrito IS DISTINCT FROM z.id_territorio_distrito;

