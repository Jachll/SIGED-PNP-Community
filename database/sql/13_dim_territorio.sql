CREATE OR REPLACE FUNCTION siged_normalize_name(input_text TEXT)
RETURNS VARCHAR(150)
LANGUAGE sql
IMMUTABLE
AS $$
    SELECT NULLIF(UPPER(REGEXP_REPLACE(BTRIM(COALESCE(input_text, '')), '\s+', ' ', 'g')), '');
$$;

CREATE OR REPLACE FUNCTION siged_slug(input_text TEXT)
RETURNS VARCHAR(150)
LANGUAGE sql
IMMUTABLE
AS $$
    SELECT COALESCE(NULLIF(REGEXP_REPLACE(COALESCE(siged_normalize_name(input_text), ''), '[^A-Z0-9]+', '_', 'g'), ''), 'SIN_DATO');
$$;

CREATE TABLE IF NOT EXISTS dim_territorios (
    id_territorio BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    codigo_territorio VARCHAR(60) NOT NULL,
    nombre_territorio VARCHAR(150) NOT NULL,
    nombre_normalizado VARCHAR(150) NOT NULL,
    tipo_territorio VARCHAR(30) NOT NULL,
    id_territorio_padre BIGINT,
    nivel_jerarquia SMALLINT NOT NULL,
    codigo_referencia VARCHAR(40),
    estado_territorio VARCHAR(20) NOT NULL DEFAULT 'ACTIVO',
    metadata JSONB NOT NULL DEFAULT '{}'::JSONB,
    created_at TIMESTAMP NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_dim_territorios_codigo
        UNIQUE (codigo_territorio),
    CONSTRAINT fk_dim_territorios_padre
        FOREIGN KEY (id_territorio_padre)
        REFERENCES dim_territorios (id_territorio),
    CONSTRAINT chk_dim_territorios_tipo
        CHECK (
            tipo_territorio IN (
                'PAIS',
                'DEPARTAMENTO',
                'PROVINCIA',
                'DISTRITO',
                'JURISDICCION_POLICIAL',
                'COMISARIA',
                'ZONA_OPERATIVA',
                'SECTOR'
            )
        ),
    CONSTRAINT chk_dim_territorios_estado
        CHECK (estado_territorio IN ('ACTIVO', 'INACTIVO'))
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_dim_territorios_tipo_nombre_padre
    ON dim_territorios (
